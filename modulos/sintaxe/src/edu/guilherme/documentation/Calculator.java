public class Calculator {
    /**
     * This method is used to sum two integers
     * 
     * @param firstNumber  this is the first parameter of the method
     * @param secondNumber this is the second parameter of the method
     * @return int the result of this method is the sum of the two numbers.
     */
    public int sum(int firstNumber, int secondNumber) {
        return firstNumber + secondNumber;
    }
}
